SELECT [LEVLEL], 컬럼명, 표현식 --LEVEL : 위에서 아래로 : 하향검색, 아래서 위로 : 상향검색
FROM 테이블명
[WHERE 조건]
[START WITH 조건] --시작점 조건
[CONNECT BY PRIOR 조건]; --상향/하향 검색시 상위/하위에 오는 조건들을 적어줌


계층 검색의 방향 결정

CONNECT BY PRIOR 컬럼명 1 = 컬럼명 2

컬럼명 2 > 컬럼명 1 방향으로 검색방향이 이루어짐 (무조건)

컬럼명 1 = 부모, 컬럼명 2 = 자식일 경우 >  상향 검색
컬럼명 1 = 자식, 컬럼명 2  = 부모일 경우 > 하향 검색 (책은 잘못 나옴)


가지치기
WHERE = 조건 >> 검색시 해당 조건을 제외하고 검색 > 그러나 그 하위 값은 나옴

CONNECT BY 
AND = 조건 >> 검색시 해당 조건을 제외하고 검색하고 그 하위 값도 모두 제외